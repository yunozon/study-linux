#!/bin/bash

# 日付とディスクの使用量とメモリの使用量を出力しファイルに保存するスクリプト
date >> diskmem.log
echo "--------------------------------------------------------" >> diskmem.log
df -h >> diskmem.log
echo "--------------------------------------------------------" >> diskmem.log
free -h >> diskmem.log
echo "--------------------------------------------------------" >> diskmem.log